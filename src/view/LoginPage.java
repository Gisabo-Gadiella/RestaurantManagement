    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
         String username = txtUsername.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    if (username.isEmpty() || password.isEmpty()) {
        lblMessage.setText("Please enter username and password.");
        return;
    }

    Connection con = null;
    PreparedStatement pst = null;
    ResultSet rs = null;

    try {
        con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/restaurant_ordering_system_db", "root", "gadiella"
        );

        String sql = "SELECT * FROM users WHERE username = ? AND password = ?";
        pst = con.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);

        rs = pst.executeQuery();

        if (rs.next()) {
            String role = rs.getString("role");

            lblMessage.setText("Logging in, please wait...");

            // ðŸ”µ Small loading delay
            javax.swing.Timer timer = new javax.swing.Timer(1500, e -> {
                MainMenuFrame mainMenu = new MainMenuFrame(role);
                mainMenu.setVisible(true);
                this.dispose(); // Close LoginFrame
            });
            timer.setRepeats(false); // Only run once
            timer.start();

        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password.", "Login Failed", JOptionPane.ERROR_MESSAGE);
            lblMessage.setText("Invalid username or password.");
        }

    } catch (Exception ex) {
        ex.printStackTrace();
        lblMessage.setText("Database error.");
    } finally {
        // ðŸ”µ Always close resources
        try { if (rs != null) rs.close(); } catch (Exception e) {}
        try { if (pst != null) pst.close(); } catch (Exception e) {}
        try { if (con != null) con.close(); } catch (Exception e) {}
    }
    }//GEN-LAST:event_btnLoginActionPerformed

